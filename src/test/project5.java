    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        String name = (String) tableModel.getValueAt(jTable1.getSelectedRow(), 0);

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/table1", "root", "");
            PreparedStatement st = con.prepareStatement("DELETE FROM `table12` WHERE name = " + name + ";");
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "Success");

            con.close();
 
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage());

        }
        if (jTable1.getSelectedRowCount() == 1) {
            tableModel.removeRow(jTable1.getSelectedRow());

        } else {
            JOptionPane.showMessageDialog(null, "Select only 1");
           
        }
    }//GEN-LAST:event_jButton4ActionPerformed

