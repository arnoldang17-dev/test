    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String address = jTextField2.getText();
        String course = jTextField3.getText();
        
        String[] tableContents = {name, address, course};
        
        if (tableContents[0].equals("")) {
            JOptionPane.showMessageDialog(null, "no input on name.");
        } else if (tableContents[1].equals("")) {
            JOptionPane.showMessageDialog(null, "no input on address.");
        } else if (tableContents[2].equals("")) {
            JOptionPane.showMessageDialog(null, "no input on course.");
        } else {
            DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
            tableModel.addRow(tableContents);

            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/table1", "root", "");
                String sql = "insert table12 (name, address, course) values (?, ?, ?)";
                PreparedStatement pst = con.prepareStatement(sql);
                pst.setString(1, name);
                pst.setString(2, address);
                pst.setString(3, course);

                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Success");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField1.requestFocus();
                con.close();

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,ex.getMessage());

            }
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

